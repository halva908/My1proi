#include <iostream>
#include <string>
using namespace std;

int main() 
{
	setlocale(LC_ALL, "rus");

	string gender, zodiac;
	int age;

	cout << "Введите пол: ";
	cin >> gender;

	cout << "Введите знак зодиака: ";
	cin >> zodiac;

	cout << "Введите возраст: ";
	cin >> age;

	cout << "Ваше предсказание:" << endl;

	// Проверяем условие для мужчины водного знака зодиака младше 40 лет
	if (gender == "м" &&
		(zodiac == "рак" || zodiac == "скорпион" || zodiac == "овен") &&
		age < 40) {
		cout << "Сегодня очень плодотворный день. Можно добиться того, что прежде казалось почти невозможным." << endl;
	}
	// Проверяем условие для женщины земного знака зодиака от 15 до 30 лет (включительно)
	else if (gender == "ж" &&
		(zodiac == "телец" || zodiac == "дева" || zodiac == "козерог") &&
		age >= 15 && age <= 30) {
		cout << "Сегодняшний вечер подходит для общения с друзьями, проведения домашних праздников и импровизированных вечеринок. "
			<< "Будет не только весело, но и интересно: найдётся дело, которое увлечёт всех." << endl;
	}
	// Все остальные случаи
	else {
		cout << "Гороскоп для вас находится в разработке. Приходите чуточку позже ;)" << endl;
	}

	return 0;
}